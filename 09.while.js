/****** print 1-10 *******/
// print 1-10 by condition i++
var i = 0;
while(i++<10){
    console.log(i);
}
// print 1-10 by condition ++i
var i = 0;
while(++i<10){
    console.log(i);
}


/**** print 0-10 ****/
// print 0-10 by condition after i++
var i= 0;
while(i<=10){
    console.log(i);
    i++;
}

/***** print 9-0 *****/
// print 0-9
var i = 10
while(i>0){
    i--;
    console.log(i);
}
// print 9-0
var i = 10;
while(i-->0){
    console.log(i);
}
